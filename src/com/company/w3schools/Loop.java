package com.company.w3schools;

public class Loop {

    public static void main(String[] args) {

        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }

        int ii = 0;
        do {
            System.out.println(ii);
            ii++;
        }
        while (ii == 5);

    }


}
