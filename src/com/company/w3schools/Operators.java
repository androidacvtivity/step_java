package com.company.w3schools;

public class Operators {

    public static void main (String[] args) {

        int x = 100 + 50;
        System.out.println(x);

        int sum1 = 100 + 50;

        System.out.println(sum1);

        int sum2 = sum1 + 250;

        System.out.println(sum2);

        int sum3 = sum2 + sum2;

        System.out.println(sum3);
    }
}
